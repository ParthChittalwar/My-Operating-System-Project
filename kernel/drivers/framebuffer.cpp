// ============================================================
// State
// ============================================================
static constexpr uint32_t CHAR_W = 8;
static constexpr uint32_t CHAR_H = 10; // 8px glyph + 2px line gap

static uint32_t* s_fb      = nullptr;
static uint32_t  s_width   = 0;
static uint32_t  s_height  = 0;
static uint32_t  s_pitch_p = 0; // pitch in pixels
static uint32_t  s_cols    = 0;
static uint32_t  s_rows    = 0;
static uint32_t  s_cur_col = 0;
static uint32_t  s_cur_row = 0;
static uint32_t  s_fg      = COLOR_WHITE;
static uint32_t  s_bg      = COLOR_DARK;

// Draw one character glyph at pixel position (px, py).
static void draw_glyph(uint32_t px, uint32_t py, char c, uint32_t fg, uint32_t bg) {
    uint8_t idx = (uint8_t)c;
    if (idx < 0x20 || idx > 0x7F) idx = 0x7F; // show block for unprintable
    const uint8_t* glyph = font8x8[idx - 0x20];

    for (uint32_t row = 0; row < CHAR_H; ++row) {
        uint8_t bits = (row < 8) ? glyph[row] : 0; // 2px blank gap at bottom
        for (uint32_t col = 0; col < CHAR_W; ++col) {
            uint32_t pixel = (bits & (0x80 >> col)) ? fg : bg;
            uint32_t y = py + row;
            uint32_t x = px + col;
            if (x < s_width && y < s_height)
                s_fb[y * s_pitch_p + x] = pixel;
        }
    }
}

static void scroll_up() {
    // Move all rows up by one character height.
    uint32_t scroll_px = CHAR_H;
    for (uint32_t y = 0; y < s_height - scroll_px; ++y)
        for (uint32_t x = 0; x < s_width; ++x)
            s_fb[y * s_pitch_p + x] = s_fb[(y + scroll_px) * s_pitch_p + x];
    // Clear the last row.
    for (uint32_t y = s_height - scroll_px; y < s_height; ++y)
        for (uint32_t x = 0; x < s_width; ++x)
            s_fb[y * s_pitch_p + x] = s_bg;
}

// ============================================================
// Public API
// ============================================================

void fb_init(void* fb_addr, uint32_t width, uint32_t height, uint32_t pitch) {
    s_fb      = reinterpret_cast<uint32_t*>(fb_addr);
    s_width   = width;
    s_height  = height;
    s_pitch_p = pitch / 4;
    s_cols    = width  / CHAR_W;
    s_rows    = height / CHAR_H;
    fb_clear();
}

void fb_set_color(uint32_t fg, uint32_t bg) { s_fg = fg; s_bg = bg; }

void fb_clear() {
    for (uint32_t y = 0; y < s_height; ++y)
        for (uint32_t x = 0; x < s_width; ++x)
            s_fb[y * s_pitch_p + x] = s_bg;
    s_cur_col = 0;
    s_cur_row = 0;
}

void fb_putchar(char c) {
    if (c == '\r') { s_cur_col = 0; return; }
    if (c == '\b') {
        if (s_cur_col > 0) --s_cur_col;
        draw_glyph(s_cur_col * CHAR_W, s_cur_row * CHAR_H, ' ', s_fg, s_bg);
        return;
    }
    if (c == '\t') {
        uint32_t next_tab = (s_cur_col + 4) & ~3u;
        while (s_cur_col < next_tab) fb_putchar(' ');
        return;
    }
    if (c == '\n') {
        s_cur_col = 0;
        ++s_cur_row;
        if (s_cur_row >= s_rows) { scroll_up(); s_cur_row = s_rows - 1; }
        return;
    }
    draw_glyph(s_cur_col * CHAR_W, s_cur_row * CHAR_H, c, s_fg, s_bg);
    ++s_cur_col;
    if (s_cur_col >= s_cols) {
        s_cur_col = 0;
        ++s_cur_row;
        if (s_cur_row >= s_rows) { scroll_up(); s_cur_row = s_rows - 1; }
    }
}

void fb_print(const char* s) { for (; *s; ++s) fb_putchar(*s); }

static void print_u64_hex(uint64_t v) {
    fb_print("0x");
    static const char hex[] = "0123456789ABCDEF";
    char buf[17]; buf[16] = 0;
    for (int i = 15; i >= 0; --i) { buf[i] = hex[v & 0xF]; v >>= 4; }
    fb_print(buf);
}
static void print_u64_dec(uint64_t v) {
    if (v == 0) { fb_putchar('0'); return; }
    char buf[21]; int i = 0;
    while (v) { buf[i++] = (char)('0' + v % 10); v /= 10; }
    for (int j = 0, k = i-1; j < k; ++j, --k) { char t=buf[j]; buf[j]=buf[k]; buf[k]=t; }
    buf[i] = 0; fb_print(buf);
}

void fb_print_hex(const char* prefix, uint64_t val) {
    fb_print(prefix); print_u64_hex(val); fb_putchar('\n');
}
void fb_print_dec(const char* prefix, uint64_t val) {
    fb_print(prefix); print_u64_dec(val); fb_putchar('\n');
}

uint32_t fb_cursor_row() { return s_cur_row; }
uint32_t fb_cursor_col() { return s_cur_col; }
